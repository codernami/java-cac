    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
            String varUsuario;
            char[] varClave;
            char[] varRepetir;
            
            varUsuario = this.txtUsuario.getText();
            varClave = this.txtClave.getPassword();
            varRepetir = this.txtRepetir.getPassword();
            
            if (String.valueOf(varClave).equals(String.valueOf(varRepetir))){
                File archivo = new File("texto.txt");
                FileWriter escribir = new FileWriter(archivo, true);
                
                escribir.write(varUsuario + "\r \n");
                escribir.write(String.valueOf(varClave + "\r \n"));
                escribir.close();
                
                JOptionPane.showMessageDialog(null, "OK");                        
            }else{
                JOptionPane.showMessageDialog(null, "No son iguales");
            }
        } catch (Exception e) {
            System.out.println("Error de archivo");
            System.out.println(e);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

